package org.example.section01;

public class assignment {
    public static void main(String[] args) {
        // 대입 연산자와 산술 복합 대입 연산자를 이해하고 활용할 수 있다.

        int num = 12;
        num = num + 3;

        num += 3;
        System.out.println(num);
        num -= 3;
        System.out.println(num);
        num *= 3;
        System.out.println(num);
        num /= 3;
        System.out.println(num);
        num %= 3;
        System.out.println(num);

        // *주의* 작성 순서 지킬 것
        // sum =- 2;

    }
}
